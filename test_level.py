    def generate_datamap(self):
        x = 0
        y = 0

        x_pos = 0
        y_pos = 0
        for row in self.filled_tilemap:
            for column in row:
                if column == 0:
                    self.datamap[y][x] = None
                if column == 1:
                    self.datamap[y][x] = tile.Tile(self.screen, x_pos, y_pos)
                x_pos += self.test_tile_width
                x += 1
            x_pos = 0
            y_pos += self.test_tile_height

            x = 0
            y += 1


    def draw_tilemap(self):
        x = 0
        y = 0
        for row in self.datamap:
            for column in row:
                if column != None:
                    self.screen.blit(column.img, (x, y))
                    column.x = x
                    column.y = y
                x += self.test_tile_width
            x = 0
            y += self.test_tile_height
                    


